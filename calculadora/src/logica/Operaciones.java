package logica;

public class Operaciones {
    //atributos
    int valorUno;
    int valorDos;

    //constructores

    public Operaciones() {

        this.valorUno = valorUno;
        this.valorDos = valorDos;
    }
    //todos

    //suma
    public int suma (int valorUno,int valorDos){
        return valorUno+valorDos;
    }

}
